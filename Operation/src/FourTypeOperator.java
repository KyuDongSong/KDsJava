
public class FourTypeOperator {
	public static void main(String[] args) {
		int additionResult = 10 + 50;
		System.out.println(additionResult);

		int substractionResult = 50 - 10;
		System.out.println(substractionResult);

		int multiplicationResult = 10 * 5;
		System.out.println(multiplicationResult);

		int divisionQuotientResult = 5 / 3;
		System.out.println(divisionQuotientResult);

		int divisionRemainderResult = 5 % 3;
		System.out.println(divisionRemainderResult);
	}
}
