package com.ktds.skd.news.vo;

public class NewsVO {

	private String title;
	private String pressCenter;
	private String name;

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getPressCenter() {
		return pressCenter;
	}

	public void setPressCenter(String pressCenter) {
		this.pressCenter = pressCenter;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
